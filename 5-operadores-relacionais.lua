-- <   >   <=  >=  ==  ~=

-- Use < para menor que
print( 2 < 5 )
print()

-- Use > para maior que
print( 20 > 5 )
print()

-- Use <= para menor igual que
print( 2 <= 5 )
print()

-- Use >= para maior igual que
print( 20 >= 5 )
print()

-- Use == para comparações de equivalencia
print( 'ivo' == 'darkside' )
print()

-- Use ~= para comparações de diferença
print( 'augusto' ~= 'didatico' )
print()

-- nil é especificamente somente igual a nil
print(pipipi == popopo)
print(pipipi == 0)
print(pipipi == false)