-- use + para somas
soma = 2 + 2

-- use - para subtrações
subtracao = 10 - 5

-- use * para multiplicações
multiplicacao = 3 * 3

-- use / para divisões
divisao = 10 / 5

-- use ^ para potenciações
potencia = 2^3

-- use % para modularizações (resto)
modulo = 5 % 2

print(soma, subtracao, multiplicacao, divisao, potencia,modulo)
print()

-- use - para modificar o sinal de um number

n1 = 1
n2 = -1
n3 = -n1
n4 = -n2
print(n1, n2, n3, n4)
